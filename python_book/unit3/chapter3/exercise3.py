""" This program will print multiples of 13 that are smaller than 100. """
# Print all multiples of 13 that are smaller than 100. USe the range function in the
# following manner: range(start,end,step) where "start" is the starting value of the
# counter, "end" is the end value  and "step" is the amount by which the couter is
# incresed each time.
for i in range(13,100,13):
    if i < 100:
        print(i)
